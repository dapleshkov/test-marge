public class Info {
    private Integer x;
    private boolean d;
    private  Integer y;

    public Info(Integer x, boolean d, Integer y) {
        this.x = x;
        this.d = d;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public boolean isD() {
        return d;
    }

    public void setD(boolean d) {
        this.d = d;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
